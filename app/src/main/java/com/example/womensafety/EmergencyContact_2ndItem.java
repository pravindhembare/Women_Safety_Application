package com.example.womensafety;

public class EmergencyContact_2ndItem {
    private String name;
    private String number;

    public EmergencyContact_2ndItem(String name, String number) {
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }
}

